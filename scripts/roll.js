var item1 = $(".gallery__rolls1");
var item2 = $(".gallery__rolls2");
var item3 = $(".gallery__rolls3");
var item4 = $(".gallery__rolls4");
var item5 = $(".gallery__rolls5");

$(function() {
// OPACITY OF BUTTON SET TO 0%
    item1.css("opacity","0");


// ON MOUSE OVER
    item1.hover(function () {

// SET OPACITY TO 70%
            $(this).stop().animate({
                opacity: .7
            }, "slow");
        },

// ON MOUSE OUT
        function () {

// SET OPACITY BACK TO 50%
            $(this).stop().animate({
                opacity: 0
            }, "slow");
        });
});
